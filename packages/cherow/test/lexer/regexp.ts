function isValidAlphaEscapeInAtom(s: any): any {
    switch (s) {
        // ClassEscape[U] :: b
        case "b":
            // ControlEscape :: one of f n r t v
        case "f":
        case "n":
        case "r":
        case "t":
        case "v":
            // CharacterClassEscape :: one of d D s S w W
        case "d":
        case "D":
        case "s":
        case "S":
        case "w":
        case "W":
            return true;
        default:
            return false;
    }
}

